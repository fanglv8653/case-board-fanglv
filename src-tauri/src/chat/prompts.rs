//! 案件 AI 助手的 system / task prompt 模板。
//!
//! 核心约束(CLAUDE.md 第 4 节):
//!   - 不得虚构事实、证据、法规、判例或法律依据
//!   - 不得把未经确认的事实写成确定事实
//!   - 涉及最终法律判断时由律师决定
//!
//! 这些约束**必须**写进 system prompt,而不是依赖 LLM 默认行为。
//! DeepSeek 在写法律内容时会有幻觉法条编号、虚构判例名的倾向。

